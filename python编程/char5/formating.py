name='张三'
age=20
print('我叫%s，我今年%i岁了' % (name , age) ) # %s字符占位符 %i或%d表示整数 %f浮点数
print('我叫{0}，我今年{1}岁了'.format(name,age))#调用字符串的formate方法
print(f'我叫{name}，我今年{age}岁了') #python3以上

print('%10d' % 98) #10表示宽度
print('%.3f' % 3.1415926) #.3标识小数点后3位
print("%10.3f" % 3.1415926)#同时设置宽度和小数
print('{0:10.3f}'.format(3.1415926))#.3标识小数点后3位